function soma(a, b) {
    return(a + b);
}

function subtracao(a, b) {
    return(a - b);
}

function multiplicacao(a, b) {
    return(a * b);
}

function divisao(a, b) {
    return(a / b);
}

function modulo(a, b) {
    return(a % b);
}

console.log(soma(20, 10));
console.log(subtracao(20, 10));
console.log(multiplicacao(20, 10));
console.log(divisao(20, 10));
console.log(modulo(20, 10));